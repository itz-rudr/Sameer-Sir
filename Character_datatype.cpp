//To Explain character datatype
#include<stdio.h>
int main(){
	//char from char
	char ch='A';
	printf("ch =%c\n",ch);
	printf("ASCII value = %d\n",ch);
	//char from int
	int ch1 = 57;
	printf("ch = %c\n",ch1);
	printf("ASCII Value = %d\n",ch1);
	//int to char
	int ch2 = 'B';
	printf("ch = %c\n",ch2);
	printf("ASCII Value = %d\n",ch2);
	//char to int
	char ch3=67;
	printf("ch = %c\n",ch3);
	printf("ASCII Value = %d\n",ch3);
}