#include<stdio.h>
int main(){
	//normal text output
	printf("Welcome\n");
	//New Line  & Tab
	printf("\nGood \tEvening\n");
	//Backspace
	printf("Welcome\b\b\ba");
	//Denotes Comment
	printf("\n");
	//To print component that is defined in compiler
	printf("SAM\"s\n");
	printf("\\n");
	printf("\nWEL\t\t\tCOME \n");
	printf("Good Morning\n");
	printf("amit\n");
	printf("\t\t\tBYE");
	printf("\n");//new line
	printf("WELCOME\t\tGOOD MORNING\n\t\t\tBYE");
	printf("123456789\n");
	printf("WELCOME\b\bA");
	printf("\nWELCOME\b\b\b\bH");//WELCOME--->WELHOME
	printf("\"bharat\"");
	printf("\n\'BHARAT\'\n");
	printf("\\\\\n");
	printf("\\n");
	printf("\\t\n");
	printf("WELCOME\t\t\t\tGOOD MORNING\t\t\t\t\t\t BYE");
}